#include <iostream>

using namespace std;
int main()
{ 
    /* do While loop in C++*/
        // Syntax:
        // do
        // {
        //     C++ statements;
        // }while(condition);
        
//  Printing 1 to 100 using while loop
    int i=1;
    do{
        cout<<i<<endl;
        i++;
    }while(i<=100);

    return 0;
}
